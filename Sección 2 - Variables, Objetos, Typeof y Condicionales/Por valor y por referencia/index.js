/* Hay dos tipo de datos:
    1)  Primitivos: Tipo de dato que apunta a un solo valor.
        a) String.
        b) boolean.
        c) undefined.
        d) null.
        e) entero.
    2) Objetos: Colección de tipo de datos primitivos u otros objetos.
    Los objetos están sujetos a la nomenclatura de pares: Cada una de las propiedades dentro de un
    objeto, tiene que tener un par. Las propiedades se separan con comas.
    Pueden tener objetos anidados.
    Cadena: Sucesión de objetos. Pueden terminar en:
        1) Una funcipon vacía.
        2 Variable primitiva.
        3) Objeto vacío.

Tipo de escritura dinámica: Las variables no están atadas a un tipo de dato particular. */