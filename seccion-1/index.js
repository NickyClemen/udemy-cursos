/* ECMAScript
    1) es la especificación para crear un lenguaje de scripting.
    2) Los navegadores utilizan ECMAScript para interpretar el código Javascript.
    3) ECMAScript dicta las reglas, detalles y lineamientos que un lenguaje de scripting debe seguir
    para ser considerado por ECMAScript. 
    
ECMAScript 5.1 - La version actual (la más soportada).
ECMAScript 2015 (ES6): El nuevo standard, nuevas funciones agregadas, y otras se declararon obsoletas. 
Algunas de sus funciones deben ser transpiladas con herramientas como Babel para que funcionen.
ECMAScript 2016 (ES7): Incluye funciones nuevas.
ECMAScript 2017 (ES8): Más funciones nuevas como Async/Await. 
ES.Next: Es la versión en desarrollo. La mayoría de sus funciones son porpuestas, pero aún no 
está finalizado.

Javascript
1) Un lengujae de scripting que sigue la especificación de ECMAScript. 
2) Es un dialecto del lenguaje ECMAScript. 
3) Javascript es el lenguaje de programación, mientras que ECMAScript son las especificaciones
que debe seguir. */