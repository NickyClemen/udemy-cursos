/* Los sets.
Históricamente, Javascript sólo ha tenido un tipo de colección de datos: Los arrays.
Y aunque muchos digan que los objetos son una colección de pares de valores, no son realmente
una colección de datos.
En otros lenguajes de programación, existen:
    1) Listas.
    2) Arrays.
    3) Colecciones.
    4) Maps.
    5) Diccionarios.
    6) etc.
    
Hay liberías de terceros que permiten utilizar otro tipo de colecciones, pero al final, terminan
en arrays u objetos comunes.

Los sets son una lista ordenada de valores sin duplicados. Permiten un rápido acceso a la data que
contienen. */