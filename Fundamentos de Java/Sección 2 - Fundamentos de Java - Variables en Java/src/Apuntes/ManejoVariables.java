package Apuntes;

public class ManejoVariables {

	public static void main(String[] args) {
		
		/* Las variables se declaran con el tipo de dato primero.
		En las últimas versiones (versión 10), se utiliza la palabra reservada VAR. */
		var despedirse = "Hasta luego";
		/* Asigna dinámicamente el tipo de dato. */
		System.out.println(despedirse);
		
	}

}
