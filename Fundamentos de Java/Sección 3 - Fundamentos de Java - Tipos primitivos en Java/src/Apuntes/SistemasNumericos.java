package Apuntes;

public class SistemasNumericos {

	public static void main(String[] args) {
		
		// Número decimal.
		int decimal = 10;
		System.out.println("El valor del número decimal es de " + decimal + ".");
		
		/* Número binario 
		Para definir un binario, hay que anteponer un 0b. */
		int binario = 0b1010;
		System.out.println("El valor del número decimal es de " + binario + ".");
		
		/* Número octal.
		Para definir un octal, hay que anteponer un 0. */
		int octal = 012;
		System.out.println("El valor del número decimal es de " + octal + ".");
		
		/* Número hexagecimal.
		Para definir un hexagecimal, hay que anteponer un 0x. */
		int hexa = 0xA;
		System.out.println("El valor del número decimal es de " + hexa + ".");
		
		// Es soportado por la palabra reservada var. Solamente cambia la literal.

	}

}
