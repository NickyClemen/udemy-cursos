/* Como Javascript es un lenguaje de tipo dinámico, no se especifican los tipos de datos de las
variables, ya que se van asignando durante el flujo de ejecución. 

Se puede reclarar typeof [variable] o typeof(variable).

Los tipos de dato se almacenan en el valor de la variable, y no en la variable. 
En JS, todos los enteros son de tipo Number. No distingue int, double ni float. 

Primitivos
    1) Number
    2) String
    3) Boolean
    4) Symbol
    5) Undefined
    6) Null
    
Objetos (tiposde dato por referencia) 
   1) Array.
   2) Object
   3) Set
   4) Map */