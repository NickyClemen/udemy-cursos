/* Categoría de patrones
    1) De creación de objetos: Cada lenguaje tiene su forma de creación de objeto, pero con
    el patrón se sigue un mismo lineamiento.
        a) Factory
        b) Abstract Factory
        c) Builder
        d) Prototype
        e) Singleton
    2) De estructura: Definen como se componen y relacionan los objetos entre sí. La ida es que, cuando haya cambios,
    impacten lo menos posible en el sistema.
        a) Adapter
        b) Bridge
        c) Composite
        d) Decorator
        e) Facade
        f) Flyweight
        g) Proxy
    3) De comportamiento: Definen la interdependencia y comunicación entre objetos. En algunas ocaciones, si un objeto
    cambia, puede afectar a otros objetos que dependen de él.
        a) Template Method
        b) Observer
        c) Mediator
        d) State
        e) Visitor
    4) De arquitectura: Definen funcionalidad específica sobre la arquitectura de una aplicación, como carpetas, nombres,
    que hace cada archivo, y como se relacionan. Arquitectura engloba los otros tres. 
        a) MVC (Model View Controller)
        b) MVP (Model View Presenter) 
        c) MVVM (Model View ViewModel)
        d) Frameworks como Maria, SpineJS, Ember, Backbone o Angular siguen esas arquitecturas. */